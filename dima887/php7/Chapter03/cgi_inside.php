/**
 * *********    Cookies    **********

  Данные, отправленные веб-сервером и хранимые в каталоге браузера в виде файла.
  Set-Cookie: name-value; expires=дата domain=ИМЯ_ХОСТА; path=путь; secure
  name -  имя куки
  value - значение куки
  expires - время жизни куки
  domain - имя хоста, с которого установили cookie
  path - путь к сценарию, установивший cookie
  secure - куки передаются только через HTTPS
