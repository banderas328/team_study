/**
 *  *********** ПЕРЕМЕННЫЕ ОКРУЖЕНИЯ  **********

    HTTP_ACCEPT - MIME-типы данных
    HTTP_REFERER - документ запустивший CGI-сценарий. Откуда пришел пользователь.
    HTTP_USER_AGENT - идентифицирует адрес браузера.
    HTTP_HOST - текущий CGI-сценарий.
    SERVER_PORT - порт сервера.
    REMOTE_ADDR - IP-адрес или домен пользователя.
    REMOTE_PORT - порт который использует браузер.
    SCRIPT_NAME - Виртуальное имя сценария. Часть URL после имени сервера и до символа ?
    REQUEST_METHOD - метод передачи данных.
    QUERY_STRING - параметры URL.
    CONTENT_LENGTH - кол-во байтов данных.
 */


