/**
 *  ****** Ссылочные переменные   *******


***  Жесткие ссылки

 $a = 10;
 $b =& $a;              //теперь $b - тоже самое, что и $a
 $b = 0;                // на самом деле a = 0
 echo "b = $b, a = $a"; // выводит "b = 0, a = 0"

 unset($b) - разрывает связь между объектом и ссылкой

***  Символические ссылки

 Строковая переменная, хранящая имя другой переменной.

 $right = "red";
 $wrong = "blue";
 $color = "right";
 echo $$color;        //выводит значение переменной $right - {"red"}
 $$color = "notBlue"; //присваевает переменной $right новое значение

***  Ссылки на объекты

 $first - new AgentSmith(); //переменная хранит не объект, а ссылку на объект
 $first->mind = 0.123;
 $second = $first;
 $second->mind = 100;
 echo "First mind: {$first->mind}, second: {$second->mind}"; // 100 и 100
*/


/**
 * ****** Возвращаемые значения   *******

 function test($var): return_type {}

 функция test() возвращает return_type:

 string: строка
 int, integer, long: целое число
 double, float, number: вещественное число
 boolean, bool: логический тип
 array: массив
 list: массив с целыми ключами, от 0 и следующими подряд
 object: объект
 null: специальный тип null
 void: ничего не возвращает. Или правельней возвращает false или пустую строку.
 mixed: любой тип
 resource: ресурс
 callback: функция обратного вызова
*/