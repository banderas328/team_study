<?php

/**
 *  ********  Типы переменных  ********

  gettype($var) - узнать тип переменной.

***  Integer(целое число)
  32-битном варианте от -2 147 483 648 до 2 147 483 647
  64-битном варианте от -9 223 372 036 854 775 807 до 9 223 372 036 854 775 807
  PHP_INT_MIN - содержин минимальное значение
  PHP_INT_MAX - содержин максимальное значение

***  double(вещественное число)
  от ±2.23*10^-308 до ±1.79*10^308
  346.1256 -> 3.461256*10^2 -> 3.461256e+2
  0.00012 -> 1.2*10^-4 -> 1.2e^-4
  INF - выход за допустимое значение
  echo 1.8e308; // INF
  NAN - недопустимое число
  echo sqrt(-1); //NAN

***  string(строка текста)
  2 Гбайт - Ограничение длины
  $str = 'hello world';

***  array(ассоциативный массив)
  Конструкция array() или [] создаёт массив

***  object(ссылка на объект)
  $a = $obj - переменная хранит ссылку на объект

***  resource(ресурс)
   Специальная переменная, содержащая ссылку на внешний ресурс.

  ftp_connect() — Устанавливает соединение с FTP-сервером
  mysql_connect() — Открывает соединение с сервером MySQL
  fopen() — Открывает файл или URL
  imagecreate() — Создание нового палитрового изображения

***  boolean(логический тип)
  true или false
 true = любое ненулевое число(и не пустая строка), ключевое слово true.
 false = 0, пустая строка, ключевое слово false.

***  null(специальное значение)
  Особый тип данных и называется null

***  callable(функция обратного вызова)
  Функция выступает в качестве переменной.
*/



/**
 *  *******  Действия с переменной   ********

