/**
 * ****** Определение типа переменной  ******

  Возвращает true если $a:
  is_int($a) - целое число
  is_infinite($a) - бесконечное число. INF
  is_nan($a) - не допустимое числовое значение. NAN
  is_string($a) - строка
  is_numeric($a) - число или строка в виде числа
  is_bool($a) - true или false
  is_scalar($a) - простой тип. Один из выше перечислинных.
  is_null($a) - значение null
  is_array($a) - массив
  is_object($a) - ссылка на объект
*/


/**
 *  ******** Установка типа переменной   *********

  settype($var, $type); $var - значение; $type - тип;

  Преобразует $var в :
  floatval($var) или double($var) - вещественное число
  strval($var) - строка
  intval($var) - целое число
*/


